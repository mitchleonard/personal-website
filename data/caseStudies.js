export function getCaseStudy(slug) {
  return caseStudies.find((cs) => cs.slug === slug)
}

export function getAdjacentCaseStudy(currentSlug) {
  const current = getCaseStudy(currentSlug)
  if (!current || !current.next) return null
  return getCaseStudy(current.next)
}
